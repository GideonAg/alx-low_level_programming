#include "3-calc.h"

/**
 * op_add - sum a and b
 * @a: parameter
 * @b: parameter
 * Return: integer
 */

int op_add(int a, int b)
{
	return ((a) + (b));
}

/**
 * op_sub - subtract a and b
 * @a: parameter
 * @b: parameter
 * Return: integer
 */

int op_sub(int a, int b)
{
	return ((a) - (b));
}

/**
 * op_mul - multiply a and b
 * @a: parameter
 * @b: parameter
 * Return: integer
 */

int op_mul(int a, int b)
{
	return ((a) * (b));
}

/**
 * op_div - divide a and b
 * @a: parameter
 * @b: parameter
 * Return: integer
 */

int op_div(int a, int b)
{
	return ((a) / (b));
}

/**
 * op_mod - remainder of the division of a by b
 * @a: parameter
 * @b: parameter
 * Return: integer
 */

int op_mod(int a, int b)
{
	return ((a) % (b));
}
